package model;

import java.util.Date;

/**
 *
 * @author DANIELLEGONCALVESPRA
 */
public class Post {
    
    private int id;
    private Date data;
    private String titulo;
    private String texto;

    public Post(int id, Date data, String titulo, String texto) {
        this.id = id;
        this.data = data;
        this.titulo = titulo;
        this.texto = texto;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getData() {
        return data;
    }

    public void setData(Date data) {
        this.data = data;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }
    
}
